# for ციკლი გამოიყენება როცა წინასწარ ვიცით რამდენჯერ გვინდა კოდის შესრულება
# while ციკლი გამოიყენება მაშინ როცა კოდის შესრულება უნდა გაგრძელდეს მანამ სანამ პირობა მართალია

# For ციკლის მაგალითი:
for i in range(5):
    print("ეს არის for ციკლი", i)

# While ციკლის მაგალითი:
i = 0
while i < 5:
    print("ეს არის while ციკლი", i)
    i += 1